<main>
  <h1>Anleitung</h1>
  
  <h2>1. Anmeldung & Spielstart</h2>
  <ul>
    <li><strong>Registrierung & Login:</strong> Du kannst Dich als registrierter Benutzer anmelden oder als Gast spielen. Als registrierter Benutzer werden Deine Ergebnisse in der Bestenliste gespeichert.</li>
    <li><strong>Spielauswahl:</strong> Wähle zuerst einen Verein aus. Anschließend wählst Du ein Team dieses Vereins und schließlich ein Spiel (mit Gegner und Spielzeit) aus. Deine Auswahl wird in Deiner Sitzung gespeichert und führt Dich ins Spiel.</li>
  </ul>
  
  <h2>2. Das Spiel</h2>
  <ul>
    <li><strong>Bingo-Feld:</strong> Beim Spielstart erscheint ein 5x5-Bingo-Feld – optimiert für Desktop, Tablet und Smartphone. Klicke auf Felder, um Ereignisse zu markieren. Nach jedem Viertel (insgesamt 4 Viertel) wird das Feld aktualisiert. Wird in einer Reihe (horizontal, vertikal oder diagonal) fünf Felder aktiviert, erscheint in der Mitte der Text „Bingo“ und Dein Erfolg wird im Scoreboard vermerkt.</li>
    <li><strong>Scoreboard:</strong> Am Spielende werden Deine Ergebnisse (aktivierte Felder, Bingos, Gewinn- und Feldquote) in der Bestenliste gespeichert und angezeigt.</li>
  </ul>
  
  <h2>3. Vorschläge</h2>
  <ul>
    <li>Im Vorschlagsbereich kannst Du eigene Ideen zu Vereinen, Teams, Bingo-Feldern oder Spielen einreichen. Deine Vorschläge werden gesammelt und nach Prüfung übernommen.</li>
  </ul>
  
  <h2>4. Admin-Bereich</h2>
  <ul>
    <li>Administratoren können Benutzer, Vereine, Teams, Spiele und Bingo-Felder verwalten, CSV-Import/Export durchführen und weitere Einstellungen vornehmen.</li>
  </ul>
  
  <h2>5. Sicherheit & SSL</h2>
  <ul>
    <li>Das Spiel läuft über HTTPS, sodass alle Daten sicher übertragen werden.</li>
  </ul>
  
  <p>Viel Spaß beim Spielen und Punkten!</p>
